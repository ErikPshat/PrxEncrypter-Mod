int is_compressed(u8 *psp_header)
{
	if (*(u16*)(psp_header+6) == 1) {
		return 1;
	}

	return 0;
}

int get_elf_size(u8 *psp_header)
{
	return *(u32*)(psp_header+0x28);
}

int get_pack_size(u8 *psp_header)
{
	return *(u32*)(psp_header+0xB0);
}

