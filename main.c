int load_elf(char *elff)
{
	FILE *fp = fopen(elff, "rb");

	if(fp == NULL) {
		return -1;
	}

	fseek(fp, 0, SEEK_END);
	int size = ftell(fp);
	fseek(fp, 0, SEEK_SET);
	fread(elf, 1, size, fp);
	fclose(fp);

    // "~PSP" header check
    if(memcmp(elf, "~PSP", 4) == 0)
    {
        printf("~PSP header detected, please decompress it!\n");
        return -1;
    }
    // ELF check
    else if(memcmp(elf, "\x7f\x45\x4c\x46", 4) != 0)
    {
        printf("File is not ELF!\n");
        return -1;
    }
    else
    {
        printf("ELF file detected: OK!\n");
    }

	return size;
}

int dumpFile(char *name, void *in, int size)
{
	FILE *fp = fopen(name, "wb");

	if(fp == NULL) {
		return -1;
	}

	fwrite(in, 1, size, fp);
	fclose(fp);

	return 0;
}

int get_kirk_size(u8 *key_hdr)
{
	// по адресу 0x70 в kirk заголовке хранится размер
	int krawSize = *(u32*)(key_hdr + 0x70);

	// 16 байтное округление до большего целого
	if(krawSize % 0x10)
	{
		krawSize += 0x10 - (krawSize % 0x10); // 16 bytes aligned
	}

	krawSize += 0x110;

	return krawSize;
}

int get_header_elf_size(u8 *psp_hdr)
{
	int size = *(u32 *)(psp_hdr + 0x28);
	return size;
}
